#include "3-calc.h"
/**
 * op_add - adds two integers.
 * @a : int 1.
 * @b : int 2.
 * Return: Results.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - difference of two integers.
 * @a : int 1.
 * @b : int 2.
 * Return: Results.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies two integers.
 * @a : int 1.
 * @b : int 2.
 * Return: Results.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides two integers.
 * @a : int 1.
 * @b : int 2.
 * Return: Results.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Remainder of a / b.
 * @a : int 1.
 * @b : int 2.
 * Return: Results.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
